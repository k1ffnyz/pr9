# Вводим два числа
a = int(input("Введите число a: "))
b = int(input("Введите число b: "))

# Проверяем, чтобы a было меньше или равно b
if a > b:
    print("Ошибка! Число a должно быть меньше или равно числу b.")
else:
    # Создаем пустой список для хранения квадратов
    squares_list = []
    
    # Проходимся по всем числам от a до b включительно
    for i in range(a, b + 1):
        # Добавляем квадрат текущего числа в список
        squares_list.append(i ** 2)
    
    # Выводим результат
    print(f"Квадраты чисел между {a} и {b}: {squares_list}")